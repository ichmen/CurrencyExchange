export function currencyCalculator(currFrom, currTo, amount, rates) {
  if (!rates) {
    return 0;
  }
  return Math.round(((amount * rates.rates[currTo]) / rates.rates[currFrom]) * 10e7) / 10e7;
}

export function getPassiveCurrencies() {
  return JSON.parse(localStorage.getItem('currencies') ?? defaultPassiveCurriencies);
}

export function setPassiveCurrencies(curriencyList) {
  localStorage.setItem('currencies', JSON.stringify(curriencyList));
}
